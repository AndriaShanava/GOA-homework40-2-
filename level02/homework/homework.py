# AND ოპერატორი:
True and True     # True   -> ორივე პირობა სწორია
True and False    # False  -> ერთ-ერთი პირობა მცდარია
False and True    # False  -> ერთ-ერთი პირობა მცდარია
False and False   # False  -> ორივე პირობა მცდარია

# OR ოპერატორი:
True or True      # True   -> საკმარისია ერთი მაინც იყოს True
True or False     # True   -> ერთი მაინც Trueა
False or True     # True   -> ერთი მაინც Trueა
False or False    # False  -> ორივე Falseა
