float_num = float(input("Enter the number: "))
int_num = int(input("Enter the number: "))

# მათემატიკური ოპერაციების შესრულება
print(f"{float_num} + {int_num} = {float_num + int_num}")
print(f"{float_num} - {int_num} = {float_num - int_num}")
print(f"{float_num} * {int_num} = {float_num * int_num}")
print(f"{float_num} / {int_num} = {float_num / int_num}")